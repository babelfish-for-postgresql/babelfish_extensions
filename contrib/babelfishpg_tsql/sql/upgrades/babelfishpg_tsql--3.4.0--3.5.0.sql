  a.pid as spid
  , null::smallint as kpid
  , coalesce(blocking_activity.pid, 0) as blocked
  , null::sys.binary(2) as waittype
  , 0::bigint as waittime
  , CAST(a.wait_event_type as sys.nchar(32)) as lastwaittype
