-- time
if CAST('01:01:01' AS TIME) < CAST(CAST('02:02:02' AS TIME) AS SQL_VARIANT) SELECT 1 ELSE SELECT 2;
GO

-- date
if CAST('2010-02-02' AS DATE) < CAST(CAST('2020-02-02' AS DATE) AS SQL_VARIANT) SELECT 1 ELSE SELECT 2;
GO

-- smalldatetime
