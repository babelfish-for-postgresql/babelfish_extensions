if CAST('2020-10-05 09:00:00.123456-9:00' AS DATETIMEOFFSET) < CAST(CAST('2020-10-05 09:00:00.123456-8:00' AS DATETIMEOFFSET) AS SQL_VARIANT) SELECT 1 ELSE SELECT 2;
GO

